    base_amount           bigint        not null default 0,
    reference_no          varchar(100),
    notes                 text,
    discount_type         varchar(10),
    discount_value        bigint        not null default 0,
    status                varchar(20)   not null default 'Draft',
    status_history        jsonb         not null default '[]'::jsonb,

    created_at            timestamptz   not null default now(),
    created_by            char(26),
    updated_at            timestamptz   not null default now(),
    updated_by            char(26),
    deleted_at            timestamptz,
    version               bigint        not null default 0,

    constraint ck_amount_received_amount_positive check (amount_amount > 0),
    constraint ck_amount_received_discount_type
        check (discount_type is null or discount_type in ('percent','amount')),
    constraint ck_amount_received_status
        check (status in ('Draft','Approved','Confirmed','Voided'))
);

create index ix_amount_received_supplier_id   on amount_received (supplier_id);
create index ix_amount_received_po_id          on amount_received (po_id);
create index ix_amount_received_status         on amount_received (status);
create index ix_amount_received_received_date  on amount_received (received_date);

