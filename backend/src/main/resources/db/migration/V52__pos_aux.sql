from roles r
join permissions p on p.code in (
    'pos.parked_sale.read', 'pos.parked_sale.write',
    'pos.refund.read', 'pos.refund.write', 'pos.refund.no_receipt',
    'pos.event.read', 'pos.event.write')
where r.code = 'admin';
