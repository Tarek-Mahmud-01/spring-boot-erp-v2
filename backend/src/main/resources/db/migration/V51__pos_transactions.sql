-- Permissions for the POS "transactions" slice (pos.<concept>.read/.write),
-- granted to the admin role so the @PreAuthorize checks are reachable.
-- Fresh PERM126/PERM128 ULIDs (exactly 26 chars) — collision-free vs the
-- existing seeded PERM01..PERM110 and V50 pos/registers, which claims
-- PERM120..124 AND PERM127 (pos.manager.operate) — NOT a contiguous
-- PERM120..125 block — plus V52 pos/aux PERM132..138. PERM127 is therefore
-- skipped here; PERM125/129/130/131 remain free for future slices.
-- =====================================================================
insert into permissions (public_id, code, name, module, description) values
  ('01J0AA0000000000000PERM126', 'pos.transaction.read',  'View POS transactions', 'pos', 'List, filter, and view POS sales/refunds, their lines, and tenders'),
  ('01J0AA0000000000000PERM128', 'pos.transaction.write', 'Operate POS sales',     'pos', 'Open a cart, add/edit/void lines, tender payment, complete/void, park/resume a sale');

insert into role_permissions (role_id, permission_id)
select r.id, p.id
from roles r
join permissions p on p.code in (
    'pos.transaction.read', 'pos.transaction.write')
where r.code = 'admin';
