insert into role_permissions (role_id, permission_id)
select r.id, p.id
from roles r
join permissions p on p.code in (
    'crm.customer.read', 'crm.customer.write', 'crm.customer.consent.write',
    'crm.segment.read', 'crm.segment.write', 'crm.segment.member.write')
where r.code = 'admin';
