  ('01J0AA000000000000PERM08', 'settings.numbering.write', 'Manage numbering rules', 'settings', 'Create, update, and allocate numbering rules');

insert into role_permissions (role_id, permission_id)
select r.id, p.id
from roles r
join permissions p on p.code in ('settings.numbering.read', 'settings.numbering.write')
where r.code = 'admin';
