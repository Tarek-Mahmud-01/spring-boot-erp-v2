insert into role_permissions (role_id, permission_id)
select r.id, p.id
from roles r
join permissions p on p.code in (
    'product.pricelist.read',
    'product.pricelist.write',
    'product.pricehistory.read',
    'product.pricehistory.write',
    'product.locationoverride.read',
    'product.locationoverride.write')
where r.code = 'admin';
